//////////////////////////////////////// MIDDLEWARE TO VERIFY TOKEN ////////////////////////////////////
function middleware(app, call) {

    app.use(async(req, res, next) => {

        const verify = await verifyToken(req)

        if(!verify) return res.status(401).json({ success: false, data: null, message: 'Unauthorized' })

        next()

        call()

    })

}
